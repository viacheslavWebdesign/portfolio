            <h2><span>Причина #2.</span> Фокус на негативе и текущей ситуации.</h2>
            <p>Большинство из нас погрязло в потоке непрерывных новостей. Чаще всего эти новости – с негативным окрасом, что, естественно, погружает нас в депрессивное состояние. Тем более, ни для кого не секрет, что когда каждый новый день начинается с просмотра новостей, то складывается впечатление, что абсолютно всё вокруг – плохо.</p>
            <p>Это не только фокусирует внимание клиента на проблемах, но и заставляет задумываться исключительно о способах выживания в парадигме “здесь и сейчас”. Клиенты перестают думать логично, рассматривать среднесрочную и долгосрочную перспективу и, самое ужасное – не видят в текущей ситуации новых, ранее недоступных возможностей.</p>
            <h3>Что делать?</h3>
            <ul>
               <li>Старайтесь фокусировать внимание человека не на проблемах, а на возможностях. Клиенты перестали платить? Это шанс сделать бизнес-модель более гибкой и жизнеспособной, срезать лишние затраты. Работодатель перестал платить оклад или даже уволил? Это возможность найти более перспективное направление, проанализировать свою карьеру и обучиться новым профессиям, стать лучше. Паника и стресс? В этом состоянии находится почти весь рынок, а значит для клиента это возможность очнуться из комы, оглянуться и воспользоваться возможностями, которые другие не видят (потому что просто не смотрят).</li>
               <li>Переведите мысль клиента из краткосрочной перспективы в среднесрочное планирование. Да, сейчас всем непросто. Но если думать лишь о том, что покушать сегодня и купить завтра на ужин, то очень быстро деньги закончатся, а новых поступлений как не было, так и не будет. Что клиент будет делать через месяц?</li>
               <li>Поддержите клиента морально и объясните, что вы с ним будете работать в формате синергии. Не каждый сам за себя, как сейчас это принято, а вы с ним вместе. Что ваша компания сможет обеспечить для него максимум из возможных ресурсов, предоставит дополнительные консультации и будет на связи 24/7. Что даже в случае, если всё получиться не сразу, то вместе вы точно пройдёте через этот кризис и выйдете из него сильнее, умнее и с новым, бронебойным опытом.</li>
            </ul>
            <h2><span>Причина #3.</span> Падающая платежеспособность.</h2>
            <p>Многие из догадливых читателей уже сложили причинно-следственную связь и ощутили, как на себе, так и со стороны клиентов, глобальное падание платежеспособности. Естественно, что денег стало меньше не у всех, но так или иначе кризис повлиял на доходы подавляющего числа населения.</p>
            <p>Не сложно догадаться, что если вы работаете в направлении В2С и продаете физ. лицам, то логично предположить возросшее количество возражений, связанных с наличием денег. При этом, они могут быть как явными (“Нет денег”, “После следующей зарплаты”, “Сейчас нет свободных средств”), так и скрытыми (“Я подумаю”, “Давайте после коронавируса”, “Сейчас не актуально”). Ну а в B2B продажах ситуация часто повторяется: клиенты ваших клиентов – физ. лица. Они не платят им, ваши клиенты не платят вам. Логично?</p>
            <h3>Что делать?</h3>
            <ul>
               <li>Увеличить количество попыток. Стать настойчивее, эффективнее. Перезвонить клиенту 3 раза, вместо одного, применить 5 техник работы с возражением, вместо 2. Выживут лишь самые целеустремленные.</li>
               <li>Делать максимум, зависящий от нас, не оправдываясь ситуацией на стороне клиента. Если мы берем на себя личную ответственность за свою работу, то какой бы не был рынок и его конъюнктура, мы всегда заработаем больше конкурентов.</li>
               <li>Давать максимальную пользу за минимальную стоимость. Цены услуг и продуктов неизбежно начнут снижаться, значит необходимо адаптировать ценовое предложение и продукт под новые реалии. Завлекайте клиента акциями, скидками и специальными предложениями. Делитесь бесплатными предложениями, предлагайте помощь и поддержку, отправляйте ссылки на полезные статьи и ресурсы, выстраивайте долгосрочные отношения. Предлагайте отсрочку платежа, оплату частями и работу с пост-оплатой. В таком случае, даже если сегодня ваш клиент ничего не купит, он точно запомнит вас, как самого клиентоориентированного продавца. И когда придет время покупки вашего продукта, он вспомнит о вас в первую очередь.</li>
               <li>Доносить клиенту мысль о том, что остановка в стрессовой ситуации = смерть. Единственный шанс выбраться из кризиса, обойти конкурентов и забрать их долю рынка – это поменять вектор движения, адаптироваться, мыслить гибко и на перспективу. Если просто затаиться и ждать, то ничего не поменяется и ничего не выйдет – каждый спасает себя, до нас с вами дела никому нет.</li>
               <li>Объяснить, как использование вашего продукта в текущей ситуации можно рассматривать с точки зрения не трат, а инвестирования. Например, можно <span>использовать похожий скрипт (адаптировав его под себя и свою нишу):</span></li>
            </ul>
