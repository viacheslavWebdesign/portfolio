            <h1>Чи варто давати знижки?</h1>
            <img src="assets/img/placeholder.webp" class="lazyload" data-src="assets/img/img-article.jpg">
            <p>В первой половине 2020 года большинство бизнесов столкнулись с проблемой продаж.</p>
            <p>Резкое падение на фоне наступления кризиса не коснулось разве лишь небольшую часть рынка, в то время как подавляющее большинство из нас начали испытывать трудности в ведении переговоров с клиентами.</p>
            <p>Экономика просто решила нам напомнить про свою цикличность, но, к сожалению, большинство отделов продаж оказались к этому абсолютно не готовы. И если более опытные менеджеры уже переживали экономические кризисы и могли почувствовать это на себе (а может и сделать некоторые выводы), то молодое поколение сейлзов сложно адаптируется к новым реалиям рынка.</p>
            <p>В этой статье мы разберём с вами причины падения продаж, и вы сможете получить готовые рекомендации по работе с клиентами, которые помогут вернуться к утраченным показателям.</p>
            <h2><span>Причина #1.</span> Чувство тревоги и желание безопасности.</h2>
            <p>Безопасность – одна из ключевых, фундаментальных ценностей для любого человека. Она может выражаться в желании получить стабильность, желании знать, что будет завтра и ощущении того, что в ближайшем будущем моей жизни ничего не угрожает. Но в период кризиса для подавляющего большинства (как людей, так и бизнесов) это попросту невозможно. Поэтому у людей не только обостряется желание вернуться в состояние безопасности, но и усиливается чувство тревоги.</p>
            <p>Как следствие, клиенты испытывают недоверие (даже к проверенным поставщикам или подрядчикам), отказываются от новых проектов и замирают в нерешительности. Так сказать, тактическая способность “притвориться мёртвым”. И несмотря на то, что эта тактика редко помогает выжить в условиях рыночной экономики, напуганному человеку в состоянии тревоги объяснить это практически нереально.</p>
            <h3>Что делать?</h3>
            <p>Наша первостепенная задача будет состоять в том, чтобы вернуть человека в состояние спокойствия. Или, как минимум, приблизить его к этому чувству. Самый простой способ сделать это следует из принципов психологии.</p>
            <ul>
               <li>Мы должны удостовериться, что сами не испытываем стрессовое состояние или, как минимум, не транслируем его на клиента (его не видно/слышно со стороны). Послушайте свою речь в диалоге с клиентом, сделайте ваш голос чуть более жизнерадостным и уверенным. Например, попробуйте улыбаться во время диалога и говориться более громко, эмоционально и чётко. Это минимум, который позволит клиенту услышать, что вы звоните не добавить проблем в его жизнь или бизнес, а являетесь своеобразным островком безопасности, на котором можно спокойно переждать бурю.</li>
               <li>Дайте клиенту понять, что вы на его стороне. Войдите в его положение, посмотрите на ситуацию его глазами. Это ни в коем случае не означает, что с клиентом нужно согласиться и уйти в глубокую депрессию. Наша задача – понять клиента и сказать ему, что мы понимаем, почему он испытывает такие чувства. Донести мысль, что мы сами проходили через такую же ситуацию, что мы видим его затруднения и можем помочь ему справится с ними вместе.</li>
               <li>Покажите, как покупка вашего продукта позволит клиенту вернуть себя в состояние безопасности. Например, при работе с некоторыми своими клиентами, я использую <span>вот такой скрипт:</span></li>
            </ul>
