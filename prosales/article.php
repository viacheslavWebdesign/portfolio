            <h2><span>Итог</span></h2>
            <p>Наша задача в ближайшее время – не расслабляться и уходить на каникулы, а максимально быстро адаптироваться под новый рынок, мыслить гибко и давать максимальную ценность. Те, чей продукт в глазах клиента будет максимально клиентоориентирован и те продавцы, которые будут эту клиентоориентированность транслировать – останутся на рынке и выйдут из кризиса сильнее, богаче и с большей долей рынка.</p>
