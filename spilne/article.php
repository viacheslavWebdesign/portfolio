						<b>The Sounds of the City exhibition opened at the Ukrainian House National Center on November 18 in Kyiv. </b>
						<br> <br>
						"Listen, be near and feel Ukraine!" - a call for an audiovisual tour of the homeland of the Motherland. Each city in the form of an art object embodies the memories and reflections of Ukrainians, who are connected by a kaleidoscope of authentic sounds. Thus, the sounds of 7 cities of Ukraine were recorded and transformed into seven sculptures with the help of special programs of sound design and 3D-visualization.
